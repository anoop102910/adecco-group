const mongoose = require("mongoose");

const notepadSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const notepadModel = mongoose.model("Notepad", notepadSchema);

module.exports = notepadModel;
